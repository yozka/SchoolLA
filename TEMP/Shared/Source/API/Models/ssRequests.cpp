#include "ssRequests.h"
///----------------------------------------------------------------------------



///----------------------------------------------------------------------------
using namespace API;
///----------------------------------------------------------------------------






 ///---------------------------------------------------------------------------
///
/// constructor
/// 
///
///----------------------------------------------------------------------------
ARequests :: ARequests(QObject *parent) 
	: 
		QObject(parent)
{

}
///----------------------------------------------------------------------------






 ///---------------------------------------------------------------------------
///
/// 
/// 
///
///----------------------------------------------------------------------------
void ARequests :: setGet(QString get)
{
	if (mGet == get)
		return;

	mGet = get;
	emit getChanged(get);
}
///----------------------------------------------------------------------------






 ///---------------------------------------------------------------------------
///
/// 
/// 
///
///----------------------------------------------------------------------------
void ARequests :: setGetDetails(QString getDetails)
{
	if (mGetDetails == getDetails)
		return;

	mGetDetails = getDetails;
	emit getDetailsChanged(getDetails);
}
///----------------------------------------------------------------------------






 ///---------------------------------------------------------------------------
///
/// 
/// 
///
///----------------------------------------------------------------------------
QString ARequests::get() const
{
	return mGet;
}
///----------------------------------------------------------------------------






 ///---------------------------------------------------------------------------
///
/// 
/// 
///
///----------------------------------------------------------------------------
QString ARequests::getDetails() const
{
	return mGetDetails;
}